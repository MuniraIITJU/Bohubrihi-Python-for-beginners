# Listing method
# index method
places_visited = ['India', 'Nepal', 'Bhutan', 'USA']
print(places_visited.index('Bhutan'))

# append method
places_visited.append('Bangladesh')
print(places_visited)

# insert method
places_visited.insert(2, 'Africa')
print(places_visited)

# remove method
places_visited.remove('Africa')
print(places_visited)

# Sort Method
places_visited.sort()
print(places_visited)



